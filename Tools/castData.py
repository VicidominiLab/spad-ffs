def castData(data, type='int64'):
    """
    Convert data array to type cast
    ==========  ===============================================================
    Input       Meaning
    ----------  ---------------------------------------------------------------
    data        Data array
    ==========  ===============================================================

    ==========  ===============================================================
    Output      Meaning
    ----------  ---------------------------------------------------------------
    data        Same data array, but typecasted
    ==========  ===============================================================

    """
    data = data.astype(type)
    return data
